def render_banner() -> str:
    """Render gradient-colored ASCII art banner with physics cascade graph."""
    lines = []
    # Render the particle graph (left) in bright cyan and CASCADE in gradient
    for i, line in enumerate(ASCII_ART):
        if "в–Ҳ" in line or "в•ҡ" in line:
            # Find the split point where the massive text starts
            idx = line.find("в–Ҳ") if "в–Ҳ" in line else line.find("в•ҡ")
            left_part = line[:idx]
            right_part = line[idx:]
            color = GRADIENT[i % len(GRADIENT)]
            # Left graph: cyan, Right text: bold gradient
            lines.append(f"{CYAN}{left_part}{BOLD}{color}{right_part}{RESET}")
        else:
            lines.append(f"{CYAN}{line}{RESET}")

    return "\n".join(lines)


def render_status_bar(provider: str, model: str) -> str:
    """Render the metadata box UI."""
    top    = " в•ӯв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•®"
    middle = f" в”Ӯ  вқ–  HEP Agentic Orchestrator v{VERSION}    в”Ӯ    {provider}  в”Җв”Җ  {model}"

    # Calculate padding to ensure the right border aligns exactly
    visible_len = len(f" в”Ӯ  вқ–  HEP Agentic Orchestrator v{VERSION}    в”Ӯ    {provider}  в”Җв”Җ  {model}")
